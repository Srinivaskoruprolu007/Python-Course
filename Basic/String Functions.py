name = 'srinivas'
# length function
print(len(name))  # it gives the length of the string

# ends with function
# this function tells whether the variable string ends with that specific characters
print(name.endswith('vas'))  # which results True

# starts with function
# this function tells whether the String starts with that specific characters
print(name.startswith('sri'))  # which results True

# count function
print(name.count('s'))  # it gives the count of occurrence of special character

# capitalize
print(name.capitalize())

# upper case
print(name.upper())

# lower case
print(name.lower())

# case fold
print(name.casefold())

greeting = 'Hello how are you'
# find function
print(greeting.find("Hello"))

# replace function
print(greeting.replace('Hello', 'Good'))
