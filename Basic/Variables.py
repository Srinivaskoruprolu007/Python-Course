# variables are names to store some values
""" It can store integers, float, string, boolean, set, tuple, list and etc
 """
# creating a variable
a = 10
print(a)
# you can check the type of the variable by using type() function
print(type(a))  # <class 'int' >

# float value to variable
b = 10.5
print(b)
print(type(b))  # < class 'float'>

# string value to variable
s = "My name is Srinivas"
print(s)
print(type(s))  # <class 'str'>

# list assigned to a variable
l1 = [1, 3, 4, 5, 6]
print(l1)
print(type(l1))  # < class 'list' >

# let's store a tuple to a variable
tup = (1, 4, 6, 3, 6)
print(tup)
print(type(tup))

