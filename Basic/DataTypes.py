# Integer
a = 5
print(type(a))

# float
b = 19.6
print(type(b))

# complex number
c = 2+4j
print(type(c))

# boolean
d = True
print(type(d))

# set
s = {'s', 'r', 'i'}
print(type(s))

# list
list1 = [2, 5, 6]
print(type(list1))

# tuple
tuple1 = (4, 7, 8, 5)
print(type(tuple1))

# dictionary
x = {"key": 5}
print(type(x))

# string
s = 'python programming'
print(type(s))

# tuple
s1 = 3, 6, 6, 7
print(type(s1))
